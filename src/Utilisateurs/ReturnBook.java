    private void txtSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtSearchActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tbListBr.getModel();
        //sql request
        String sql = "select * "
                + "FROM emprunts a "
                +"WHERE (a.id_adherent = ? AND LOWER(a.nom_livre) LIKE LOWER(?))";
         model.setRowCount(0);
        try{
            //prepare request
            pst = conn.prepareStatement(sql);
            pst.setString(1, id_adherent.getText());
//            pst.setString(2, id_book.getText());
            pst.setString(2, "%" + id_book.getText() + "%");
            rs = pst.executeQuery();
            if(rs.next()){
                int id_emprunt = rs.getInt("id_emprunt");
                String isbn = rs.getString("ISBN");
                String id_adherent = rs.getString("id_adherent");
                String nom_livre = rs.getString("nom_livre");
                String date_emprunt = rs.getString("date_emprunt");
                String date_retour = rs.getString("date_retour");
                String nom_adherent = rs.getString("nom_adherent");
                
                model.addRow(new Object[]{
                id_emprunt,         // ID_emprunt
                isbn,         // isbn
                nom_livre,         // nom_livre quantity
                id_adherent,           // id_adherent
                nom_adherent,          // nom_adherent
                date_emprunt,         // date_emprunt
                nom_adherent,        // nom_adherent
                        
            });

            }else{
                JOptionPane.showMessageDialog(null, "etudiant inexistant");
                rs.close();
                pst.close();
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }finally{
            try{
                if (rs != null) rs.close(); 
                if (pst != null) pst.close();
            }catch(SQLException ex){
                JOptionPane.showMessageDialog(null, ex);
            }
        }
    }//GEN-LAST:event_txtSearchActionPerformed

