    //update the book number from bd
    public void updateBookCount(){
        //get ISBN BOOK to update
        String book = ISBN.getText();
        //preapre request
        String sql = "update Livre set quantite=quantite+1 where ISBN=?";
        try{
            pst = conn.prepareStatement(sql);
            pst.setString(1, book);
            //execute request
            int rowCount = pst.executeUpdate();
            if (rowCount > 0) {
                JOptionPane.showMessageDialog(null, "return approved!");
            } else {
                JOptionPane.showMessageDialog(null, "Error in updating Book count");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error in updating Book");
        }finally{
            try{
                if (rs != null) rs.close(); 
                if (pst != null) pst.close();
            }catch(SQLException ex){
                JOptionPane.showMessageDialog(null, ex);
            }
        }
    }
    //update the Borrow number of guest
    public void updateLaonUser(){
        //get id guest
        String id = id_adherent.getText();
        //sql request
        String sql = "update Adherent set nbre_emprunt=nbre_emprunt-1 where id_adherent=?";
        try{
            //prepare request
            pst = conn.prepareStatement(sql);
            pst.setString(1, id);
            int rowCount = pst.executeUpdate();
            if (rowCount > 0) {
                JOptionPane.showMessageDialog(null, "Emprunt supprimé avec succès !");
            } else {
                JOptionPane.showMessageDialog(null, "Error in updating Book count");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error in updating Book");
        }finally{
            try{
                //close db request
                if (rs != null) rs.close(); 
                if (pst != null) pst.close();
            }catch(SQLException ex){
                JOptionPane.showMessageDialog(null, ex);
            }
        }
    }
    
    
