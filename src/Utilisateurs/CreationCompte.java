    private void signUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signUpActionPerformed
        // TODO add your handling code here:
        String user=txtName.getText();
        String pwd= new String(txtMdp.getPassword());
        
        if(user.isEmpty()||pwd.isEmpty()){
            JOptionPane.showConfirmDialog(this, "Veuillez remplir tous les champs!!!");
            return;
        }

        try{
            conn = JavaConnect.connectDB();            
            String sql="SELECT * FROM Admin WHERE nom_admin=? AND mdp_admin=? ";
            pst=conn.prepareStatement(sql);
            pst.setString(1, user);
            pst.setString(2, pwd);
            rs=pst.executeQuery();
            
            if(rs.next()){
                JOptionPane.showMessageDialog(this,"Bienvenue Ã  vous!");
                this.dispose();
                new Accueil().setVisible(true);
                }else{
                    JOptionPane.showMessageDialog(this,"Nom d'utilisateur ou Mot de passe incorrect");
                    txtName.setText("");
                    txtMdp.setText("");
                        }
            }catch(SQLException e){
                JOptionPane.showMessageDialog(this,"Erreur: "+e.getMessage());
                txtName.setText("");
                txtMdp.setText("");
                
                    }finally{
                        try{
                            if(rs!=null)rs.close();
                            if(pst!=null)pst.close();
                            if(conn!=null)conn.close();
                            }catch(Exception e){
                            }
        }

    }//GEN-LAST:event_signUpActionPerformed

