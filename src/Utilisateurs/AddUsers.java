    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        //retrieve value of all fields
        String Fname = txtFname.getText();
        String Lname = txtLname.getText();
        String email = txtEmail.getText();
        String role = txtRole.getText();

        java.util.Date dateChoisie = txtDate.getDate();

        if (dateChoisie == null || Fname.isEmpty() || Lname.isEmpty() || email.isEmpty() || role.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Veuillez remplir tous les champs!");
            return;
        }

        //Formatage de la date pour SQLite
        SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
        String date = sdf.format(dateChoisie);
        try {
            conn = JavaConnect.connectDB();
            //Insert data to table
            String sql = "INSERT INTO Adherent(nom_adherent,prenom_adherent,email_adherent,role_adherent,date_erg) VALUES(?,?,?,?,?)";
            pst = conn.prepareStatement(sql);
            pst.setString(1, Fname);
            pst.setString(2, Lname);
            pst.setString(3, email);
            pst.setString(4, role);
            pst.setString(5, date);

            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Adh√©rent ajout√© avec succ√®s!");
            
            //empty all fields
            txtFname.setText("");
            txtLname.setText("");
            txtEmail.setText("");
            txtRole.setText("");
            

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erreur:" + e.getMessage());
        } finally {
            try {
                //close db and query
                if (pst != null) {
                    pst.close();
                }
                if (conn != null) {
                    conn.close();
                }
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }

    }//GEN-LAST:event_btnSaveActionPerformed

